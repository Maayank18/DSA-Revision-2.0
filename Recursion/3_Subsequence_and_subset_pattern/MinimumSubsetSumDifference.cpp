// Minimum Subset Sum Difference
// Problem - Partition an array into two subsets such that the absolute difference of their sums is minimized.

// What it means
// Find a way to divide the array into two parts where the difference between their sums is as small as possible.

// Constraints
// - Array length: 0 <= n <= 100
// - Array elements: positive integers
// - Sum of array: 0 <= sum <= 10000
// - Time complexity: O(n * sum/2)

// Example
// Input: arr = [1, 5, 11, 5]
// Output: 0
// Partitions: [11] = 11 and [5, 5, 1] = 11, difference = 0

// Test cases
// [] -> 0
// [1] -> 1
// [1, 1] -> 0
// [1, 5, 11, 5] -> 0
// [1, 2, 3] -> 0

