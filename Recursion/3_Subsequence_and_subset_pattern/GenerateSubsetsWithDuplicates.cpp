// Generate Subsets with Duplicates
// Problem - Generate all unique subsets of an array that contains duplicates.

// What it means
// Create all combinations while avoiding duplicate subsets.

// Constraints
// - Array length: 0 <= n <= 15
// - Array may contain duplicate elements
// - Time complexity: O(n * 2^n)
// - Space complexity: O(n * 2^n)

// Example
// Input: arr = [1, 2, 2]
// Output: [[], [1], [2], [1,2], [2,2], [1,2,2]]

// Test cases
// [] -> [[]]
// [1] -> [[], [1]]
// [1, 1] -> [[], [1], [1,1]]
// [1, 2, 2] -> [[], [1], [2], [1,2], [2,2], [1,2,2]]

