// Subsequence Sum Equals K
// Problem - Check if there exists a subsequence with sum equal to k.

// What it means
// Determine whether any subsequence of the array has a sum equal to the target value k.

// Constraints
// - Array length: 0 <= n <= 20
// - Target sum k: 0 <= k <= 1000
// - Array elements: non-negative integers
// - Time complexity: O(2^n)

// Example
// Input: arr = [1, 2, 1], k = 2
// Output: true
// Subsequences with sum 2: [2], [1, 1]

// Test cases
// [], k = 0 -> true
// [1], k = 1 -> true
// [1], k = 2 -> false
// [1, 2, 1], k = 2 -> true

