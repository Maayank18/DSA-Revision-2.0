// Equal Partition Subset
// Problem - Check if an array can be partitioned into two subsets with equal sum.

// What it means
// Divide the array into two subsets such that the sum of elements in both subsets is equal.

// Constraints
// - Array length: 0 <= n <= 100
// - Array elements: positive integers
// - Sum of array: 0 <= sum <= 10000
// - Time complexity: O(n * sum/2)

// Example
// Input: arr = [1, 5, 11, 5]
// Output: true
// Partitions: [11] and [5, 5, 1]

// Test cases
// [] -> true
// [1] -> false
// [1, 1] -> true
// [1, 5, 11, 5] -> true
// [2, 2, 1, 1] -> false

