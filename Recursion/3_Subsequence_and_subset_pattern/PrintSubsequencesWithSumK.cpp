// Print Subsequences with Sum K
// Problem - Print all subsequences of an array that have a sum equal to k.

// What it means
// Find and display all subsequences whose elements sum to a target value k.

// Constraints
// - Array length: 0 <= n <= 20
// - Target sum k: 0 <= k <= 1000
// - Time complexity: O(2^n)
// - Space complexity: O(n)

// Example
// Input: arr = [1, 2, 1], k = 2
// Output: [2], [1, 1]

// Test cases
// [], k = 0 -> []
// [1], k = 1 -> [1]
// [1, 2, 1], k = 2 -> [2], [1, 1]
// [1, 1, 1], k = 2 -> [1, 1]

