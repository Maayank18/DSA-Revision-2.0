// Subset Sum
// Problem - Check if there exists a subset of an array with a given sum.

// What it means
// Determine whether any subset of the array has a sum equal to the target value.

// Constraints
// - Array length: 0 <= n <= 100
// - Target sum: 0 <= sum <= 10000
// - Array elements: non-negative integers
// - Time complexity: O(n * sum)

// Example
// Input: arr = [3, 34, 4, 12, 5, 2], target = 9
// Output: true
// Subset: [4, 5]

// Test cases
// [], target = 0 -> true
// [1], target = 1 -> true
// [1], target = 2 -> false
// [3, 34, 4, 12, 5, 2], target = 9 -> true

