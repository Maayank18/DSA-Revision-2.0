// Count Subsets with Given Sum
// Problem - Count the number of subsets of an array with a given sum.

// What it means
// Find all subsets whose elements sum to a target value and count them.

// Constraints
// - Array length: 0 <= n <= 20
// - Array elements: positive integers
// - Target sum: 0 <= sum <= 1000
// - Time complexity: O(n * sum)

// Example
// Input: arr = [1, 2, 3], target = 3
// Output: 2
// Subsets: [3], [1, 2]

// Test cases
// [] -> 0 (no subsets)
// [1], target = 1 -> 1 ([1])
// [1, 1], target = 2 -> 1 ([1, 1])
// [1, 2, 3], target = 3 -> 2

