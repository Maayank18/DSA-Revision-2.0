// Height (Maximum Depth) of Binary Tree
// Problem - Find the maximum depth (height) of a binary tree.

// What it means
// Calculate the longest path from root to any leaf node.

// Constraints
// - Tree nodes: 0 <= n <= 10^4
// - Tree height: 0 <= h <= 10^4
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [3,9,20,null,null,15,7]
// Output: 3

// Test cases
// [] -> 0
// [1] -> 1
// [3,9,20,null,null,15,7] -> 3
// [1,null,2] -> 2

