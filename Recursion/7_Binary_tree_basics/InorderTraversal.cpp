// Inorder Traversal
// Problem - Traverse a binary tree in inorder (Left-Root-Right) using recursion.

// What it means
// Visit nodes in order: process left subtree, then node, then right subtree.

// Constraints
// - Tree nodes: 0 <= n <= 100
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,null,2,3]
// Output: [1,3,2]

// Test cases
// [] -> []
// [1] -> [1]
// [1,null,2,3] -> [1,3,2]
// [1,2,3] -> [2,1,3]

