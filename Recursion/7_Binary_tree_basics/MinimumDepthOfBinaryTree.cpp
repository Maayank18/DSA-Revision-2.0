// Minimum Depth of Binary Tree
// Problem - Find the minimum depth (shortest path to leaf) of a binary tree.

// What it means
// Calculate the shortest path from root to any leaf node.

// Constraints
// - Tree nodes: 0 <= n <= 10^5
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [3,9,20,null,null,15,7]
// Output: 2

// Test cases
// [] -> 0
// [1] -> 1
// [3,9,20,null,null,15,7] -> 2
// [1,2,3,4,5,6] -> 2

