// Preorder Traversal
// Problem - Traverse a binary tree in preorder (Root-Left-Right) using recursion.

// What it means
// Visit nodes in order: process node, then left subtree, then right subtree.

// Constraints
// - Tree nodes: 0 <= n <= 100
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,null,2,3]
// Output: [1,2,3]

// Test cases
// [] -> []
// [1] -> [1]
// [1,null,2,3] -> [1,2,3]
// [1,2,3] -> [1,2,3]

