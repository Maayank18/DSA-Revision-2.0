// Postorder Traversal
// Problem - Traverse a binary tree in postorder (Left-Right-Root) using recursion.

// What it means
// Visit nodes in order: process left subtree, then right subtree, then node.

// Constraints
// - Tree nodes: 0 <= n <= 100
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,null,2,3]
// Output: [3,2,1]

// Test cases
// [] -> []
// [1] -> [1]
// [1,null,2,3] -> [3,2,1]
// [1,2,3] -> [2,3,1]

