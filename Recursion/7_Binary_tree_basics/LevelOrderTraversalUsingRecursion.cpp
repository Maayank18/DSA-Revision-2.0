// Level Order Traversal Using Recursion
// Problem - Traverse tree level by level using recursion.

// What it means
// Process all nodes at current level before moving to next level.

// Constraints
// - Tree nodes: 0 <= n <= 1000
// - Tree height: 0 <= h <= 10
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [3,9,20,null,null,15,7]
// Output: [[3],[9,20],[15,7]]

// Test cases
// [] -> []
// [1] -> [[1]]
// [3,9,20,null,null,15,7] -> [[3],[9,20],[15,7]]

