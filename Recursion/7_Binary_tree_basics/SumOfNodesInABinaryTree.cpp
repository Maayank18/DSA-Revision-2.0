// Sum of Nodes in a Binary Tree
// Problem - Find the sum of all node values in a binary tree.

// What it means
// Calculate the total sum of all values in the tree.

// Constraints
// - Tree nodes: 0 <= n <= 10^4
// - Node values: -1000 <= val <= 1000
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [3,9,20,null,null,15,7]
// Output: 54

// Test cases
// [] -> 0
// [1] -> 1
// [3,9,20,null,null,15,7] -> 54
// [1,2,3] -> 6

