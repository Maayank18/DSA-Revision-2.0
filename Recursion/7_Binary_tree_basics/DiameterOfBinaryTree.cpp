// Diameter of Binary Tree
// Problem - Find the diameter (longest path) of a binary tree.

// What it means
// The diameter is the longest path between any two nodes in the tree.

// Constraints
// - Tree nodes: 1 <= n <= 10^4
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,2,3,4,5]
// Output: 3
// Path: 4-2-1-3

// Test cases
// [1,2,3] -> 2
// [1,2,3,4,5] -> 3
// [1,2] -> 1

