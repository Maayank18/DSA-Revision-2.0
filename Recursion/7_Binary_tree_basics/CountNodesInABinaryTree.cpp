// Count Nodes in a Binary Tree
// Problem - Count the total number of nodes in a binary tree.

// What it means
// Calculate the total count of all nodes present in the tree.

// Constraints
// - Tree nodes: 0 <= n <= 10^4
// - Tree height: 1 <= h <= 10^4
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,2,3]
// Output: 3

// Test cases
// [] -> 0
// [1] -> 1
// [1,2,3] -> 3
// [1,2,3,4,5,6] -> 6

