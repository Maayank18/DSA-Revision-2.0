// Right View of Binary Tree
// Problem - Return the right view of a binary tree (rightmost node at each level).

// What it means
// Collect the last node visible from the right side at each level.

// Constraints
// - Tree nodes: 0 <= n <= 100
// - Node values: 1 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,2,3,null,5,null,4]
// Output: [1, 3, 4]

// Test cases
// [] -> []
// [1] -> [1]
// [1,2,3,null,5,null,4] -> [1, 3, 4]

