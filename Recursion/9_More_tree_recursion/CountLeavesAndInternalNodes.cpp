// What it means
// Distinguish between leaf nodes (no children) and internal nodes (have children).

// Constraints
// - Tree nodes: 1 <= n <= 1000
// - Node values: 1 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,2,3,null,null,4,5]
// Output: leaves = 3, internal = 2

// Test cases
// [1] -> leaves = 1, internal = 0
// [1,2,3,null,null,4,5] -> leaves = 3, internal = 2

