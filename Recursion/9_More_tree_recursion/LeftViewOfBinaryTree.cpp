// Left View of Binary Tree
// Problem - Return the left view of a binary tree (leftmost node at each level).

// What it means
// Collect the first node visible from the left side at each level.

// Constraints
// - Tree nodes: 0 <= n <= 100
// - Node values: 1 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,2,3,null,5,null,4]
// Output: [1, 2, 5, 4]

// Test cases
// [] -> []
// [1] -> [1]
// [1,2,3,null,5,null,4] -> [1, 2, 5, 4]

