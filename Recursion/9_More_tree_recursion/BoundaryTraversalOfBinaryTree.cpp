// Boundary Traversal of Binary Tree
// Problem - Traverse the boundary of a binary tree (left, bottom, right).

// What it means
// Collect nodes in boundary order: left boundary, leaves, right boundary (reverse).

// Constraints
// - Tree nodes: 1 <= n <= 10^4
// - Node values: 1 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,2,3,4,null,null,5]
// Output: [1, 2, 4, 5, 3]

// Test cases
// [1] -> [1]
// [1,2,3,4,null,null,5] -> [1, 2, 4, 5, 3]

