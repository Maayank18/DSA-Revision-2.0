// Binary Tree Paths
// Problem - Find all root-to-leaf paths in a binary tree.

// What it means
// Collect all paths from root to each leaf node as strings.

// Constraints
// - Tree nodes: 1 <= n <= 100
// - Node values: 1 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [1,2,3,null,5]
// Output: ["1->2->5", "1->3"]

// Test cases
// [1] -> ["1"]
// [1,2,3,null,5] -> ["1->2->5", "1->3"]
// [1,2] -> ["1->2"]

