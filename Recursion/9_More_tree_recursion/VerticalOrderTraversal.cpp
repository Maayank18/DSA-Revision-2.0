// Vertical Order Traversal
// Problem - Return vertical order traversal of a binary tree.

// What it means
// Group nodes by their column (vertical position) and traverse level by level.

// Constraints
// - Tree nodes: 1 <= n <= 1000
// - Node values: 0 <= val <= 100
// - Tree width: 1 <= width <= 1000
// - Time complexity: O(n log n)
// - Space complexity: O(n)

// Example
// Input: [3,9,20,null,null,15,7]
// Output: [[9],[3,15],[20],[7]]

// Test cases
// [3,9,20,null,null,15,7] -> [[9],[3,15],[20],[7]]
// [1,2,3,4,5,6,7] -> multiple columns

