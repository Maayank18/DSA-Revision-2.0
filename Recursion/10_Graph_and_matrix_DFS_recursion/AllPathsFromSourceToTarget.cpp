// All Paths From Source to Target
// Problem - Find all paths from source to target in a directed acyclic graph.

// What it means
// Return all possible paths that lead from node 0 to node n-1.

// Constraints
// - Graph: DAG (Directed Acyclic Graph)
// - Nodes: 2 <= n <= 15
// - Time complexity: O(2^n * n)

// Example
// Input: graph = [[1,2],[3],[3],[]]
// Output: [[0,1,3],[0,2,3]]

// Test cases
// [[1,2],[3],[3],[]] -> [[0,1,3],[0,2,3]]
// [[4,3,1],[3,2,4],[3],[4],[]] -> multiple paths

