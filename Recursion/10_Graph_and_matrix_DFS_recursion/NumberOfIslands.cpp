// Number of Islands
// Problem - Count the number of islands in a 2D grid.

// What it means
// An island is a group of '1's (land) connected 4-directionally. Count all such islands.

// Constraints
// - Grid dimensions: m x n, where 1 <= m, n <= 300.
// - Time complexity: O(m * n).

// Example
// Input: [['1', '1', '0'], ['0', '1', '0'], ['0', '0', '1']]
// Output: 2

// Test cases
// Empty grid: [] -> 0
// Single cell: [['1']] -> 1
// Multiple islands: [['1', '1', '0'], ['0', '1', '0'], ['0', '0', '1']] -> 2