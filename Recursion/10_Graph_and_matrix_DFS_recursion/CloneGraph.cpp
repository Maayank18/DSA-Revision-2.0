// Clone Graph
// Problem - Create a deep copy of an undirected graph.

// What it means
// Duplicate all nodes and edges of the graph.

// Constraints
// - Nodes: 1 <= n <= 100
// - Edges: 0 <= edges <= n*(n-1)/2
// - Time complexity: O(N + E)
// - Space complexity: O(N)

// Example
// Input: adjList = [[2,4],[1,3],[2,4],[1,3]]
// Output: [[2,4],[1,3],[2,4],[1,3]]

// Test cases
// Single node: [[]] -> [[]]
// Two nodes: [[2],[1]] -> [[2],[1]]

