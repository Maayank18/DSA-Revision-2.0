// Maximum Area of Island
// Problem - Find the maximum area of an island in a 2D grid.

// What it means
// An island is a group of '1's (land) connected 4-directionally. Find the largest such island.

// Constraints
// - Grid dimensions: m x n, where 1 <= m, n <= 300.
// - Time complexity: O(m * n).

// Example
// Input: [['1', '1', '0'], ['0', '1', '0'], ['0', '0', '1']]
// Output: 3

// Test cases
// Empty grid: [] -> 0
// Single cell: [['1']] -> 1
// Multiple islands: [['1', '1', '0'], ['0', '1', '0'], ['0', '0', '1']] -> 3