// Constraints
// - String length: 1 <= n <= 10^4
// - String contains lowercase letters
// - Time complexity: O(n)
// - Space complexity: O(n)

// Example
// Input: "abbaca"
// Output: "ca"

// Test cases
// "a" -> "a"
// "abcd" -> "abcd"
// "abbaca" -> "ca"
// "aa" -> ""

