// Reverse Linked List II
// Problem - Reverse a portion of a linked list (from position left to right).

// What it means
// Reverse only the sublist between positions left and right (1-indexed).

// Constraints
// - List length: 1 <= n <= 500
// - 1 <= left <= right <= n
// - Time complexity: O(n)
// - Space complexity: O(n) for recursion

// Example
// Input: [1,2,3,4,5], left = 2, right = 4
// Output: [1,4,3,2,5]

// Test cases
// [1,2,3,4,5], left = 2, right = 4 -> [1,4,3,2,5]
// [5], left = 1, right = 1 -> [5]
// [1,2], left = 1, right = 2 -> [2,1]

