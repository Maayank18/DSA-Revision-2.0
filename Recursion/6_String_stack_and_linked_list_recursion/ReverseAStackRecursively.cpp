// Reverse a Stack Recursively
// Problem - Reverse all elements of a stack using recursion.

// What it means
// Reverse the order of stack elements without using any data structure.

// Constraints
// - Stack size: 0 <= n <= 1000
// - Time complexity: O(n^2) worst case
// - Space complexity: O(n) for recursion

// Example
// Input: [1, 2, 3, 4]
// Output: [4, 3, 2, 1]

// Test cases
// [] -> []
// [1] -> [1]
// [1,2,3,4] -> [4,3,2,1]

