// Sort a Stack Recursively
// Problem - Sort a stack using recursion.

// What it means
// Sort stack elements without using additional data structure.

// Constraints
// - Stack size: 0 <= n <= 100
// - Stack values: -1000 <= val <= 1000
// - Time complexity: O(n^2) worst case
// - Space complexity: O(n) for recursion

// Example
// Input: [3, 1, 4, 1, 5]
// Output: [1, 1, 3, 4, 5]

// Test cases
// [] -> []
// [1] -> [1]
// [3,1,4,1,5] -> [1,1,3,4,5]

