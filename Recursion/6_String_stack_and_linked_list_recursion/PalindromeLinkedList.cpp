// Palindrome Linked List
// Problem - Check if a linked list is a palindrome.

// What it means
// Determine if a linked list reads the same forward and backward.

// Constraints
// - List length: 1 <= n <= 10^5
// - Node values: 1 <= val <= 10^5
// - Time complexity: O(n)
// - Space complexity: O(1) for optimal solution

// Example
// Input: [1,2,2,1]
// Output: true

// Test cases
// [1] -> true
// [1,2] -> false
// [1,2,2,1] -> true
// [9,9,9,9] -> true

