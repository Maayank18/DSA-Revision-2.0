// Print Linked List in Reverse
// Problem - Print all nodes of a linked list in reverse order.

// What it means
// Traverse a linked list and print its values from last to first.

// Constraints
// - List length: 0 <= n <= 10^4
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(n) for recursion stack

// Example
// Input: [1,2,3,4]
// Output: 4 3 2 1

// Test cases
// [] -> (nothing printed)
// [1] -> 1
// [1,2,3,4] -> 4 3 2 1

