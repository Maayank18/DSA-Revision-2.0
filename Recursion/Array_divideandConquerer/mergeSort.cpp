// Merge Sort (Recursive)
// Problem - Sort an array using the merge sort algorithm (divide and conquer).
//
// What it means
// Recursively split the array into halves until single-element arrays are
// reached, then merge sorted halves to produce the fully sorted array.
//
// Examples:
// Input: [3,1,4,2] -> Output: [1,2,3,4]
// Input: [] -> Output: []
//
// Constraints
// - Array length: 0 <= n <= 1e5
// - Time complexity: O(n log n)
// - Space complexity: O(n) (auxiliary merge buffer)
// - Stable sort
//
// Example
// Input: arr = [5,2,9,1]
// Output: [1,2,5,9]
//
// Test cases
// [] -> []
// [1] -> [1]
// [3,2,1] -> [1,2,3]

