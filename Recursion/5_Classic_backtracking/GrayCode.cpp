// Gray Code
// Problem - Generate n-bit Gray code sequence.

// What it means
// Gray code is a binary numeral system where two successive values differ in only one bit.
// Generate all n-bit Gray codes in order.

// Constraints
// - 0 <= n <= 16
// - Total codes: 2^n
// - Time complexity: O(2^n)

// Example
// Input: n = 2
// Output: [0, 1, 3, 2]

// Test cases
// n = 0 -> [0]
// n = 1 -> [0, 1]
// n = 2 -> [0, 1, 3, 2]
// n = 3 -> [0, 1, 3, 2, 6, 7, 5, 4]

