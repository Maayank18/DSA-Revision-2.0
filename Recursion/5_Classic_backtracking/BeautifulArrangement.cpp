// Beautiful Arrangement
// Problem - Count the number of beautiful arrangements of length n.

// What it means
// An arrangement is beautiful if the element at position i is divisible by i or i is divisible by the element.
// Count how many such valid arrangements exist for numbers 1 to n.

// Constraints
// - 1 <= n <= 15
// - Time complexity: O(n!)

// Example
// Input: n = 2
// Output: 2
// Valid: [1,2], [2,1]

// Test cases
// n = 1 -> 1 ([1])
// n = 2 -> 2 ([1,2], [2,1])
// n = 3 -> 3 ([1,2,3], [1,3,2], [2,1,3])

