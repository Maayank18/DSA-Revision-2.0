// Word Search
// Problem - Check if a word exists in a 2D board.

// What it means
// Search for a word in a grid where word letters are adjacent horizontally or vertically.

// Constraints
// - Board size: m × n (1 <= m, n <= 500)
// - Word length: 1 <= word <= 10^5
// - Time complexity: O(N * 4^L) where N is grid size and L is word length

// Example
// Input: board = [["A","B","C","E"],["S","F","C","S"],["A","D","E","E"]], word = "ABCB"
// Output: false

// Test cases
// [["A"]], word = "A" -> true
// [["A","B"]], word = "BA" -> true
// [["A","B","C","E"],["S","F","C","S"],["A","D","E","E"]], word = "ABCB" -> false

