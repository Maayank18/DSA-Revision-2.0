// Expression Add Operators
// Problem - Add operators (+, -, *, /) to string of digits to get a target value.

// What it means
// Given a numeric string and a target, find all expressions where operators between digits evaluate to the target.

// Constraints
// - String length: 1 <= n <= 11
// - Target: -2^31 <= target <= 2^31
// - Time complexity: O(3^n) (3 operators)

// Example
// Input: num = "123", target = 6
// Output: ["1+2+3", "1*2*3"]

// Test cases
// "123", target = 6 -> ["1+2+3", "1*2*3"]
// "0", target = 0 -> ["0"]
// "12", target = 3 -> ["1+2"]

