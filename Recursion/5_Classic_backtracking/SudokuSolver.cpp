// Sudoku Solver
// Problem - Solve a Sudoku puzzle by filling empty cells.

// What it means
// Fill a 9×9 grid where each row, column, and 3×3 box contains digits 1-9 exactly once.

// Constraints
// - Grid: 9×9 cells (some pre-filled)
// - Empty cells marked with 0 or '.'
// - Time complexity: O(9^(n*n)) worst case

// Example
// Input: 9×9 grid with some numbers
// Output: Completed solved grid

// Test cases
// Valid incomplete sudoku -> solved sudoku
// Invalid sudoku -> false
// Completed sudoku -> same sudoku

