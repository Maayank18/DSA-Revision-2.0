// Kth Permutation Sequence
// Problem - Find the kth permutation of the sequence 1 to n.

// What it means
// Given n and k, return the kth permutation in lexicographic order.

// Constraints
// - 1 <= n <= 9
// - 1 <= k <= n!
// - Time complexity: O(n^2) or O(n)

// Example
// Input: n = 3, k = 3
// Output: "213"

// Test cases
// n = 3, k = 1 -> "123"
// n = 3, k = 3 -> "213"
// n = 4, k = 9 -> "2314"

