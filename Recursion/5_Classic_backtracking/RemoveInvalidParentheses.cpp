// Remove Invalid Parentheses
// Problem - Remove minimum parentheses to make a valid string.

// What it means
// Find all valid combinations of parentheses after removing invalid ones.

// Constraints
// - String length: 1 <= n <= 25
// - Time complexity: O(2^n)

// Example
// Input: "()())()"
// Output: ["(())()","()()()"]

// Test cases
// "())" -> ["()"]
// ")((" -> [""]
// "()())()" -> ["(())()","()()()"]
// "()(" -> ["()"]

