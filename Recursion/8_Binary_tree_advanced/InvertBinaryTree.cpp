// Invert Binary Tree
// Problem - Invert (mirror) a binary tree.

// What it means
// Swap the left and right children of every node in the tree.

// Constraints
// - Tree nodes: 0 <= n <= 100
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [4,2,7,1,3,6,9]
// Output: [4,7,2,9,6,3,1]

// Test cases
// [] -> []
// [1] -> [1]
// [4,2,7,1,3,6,9] -> [4,7,2,9,6,3,1]

