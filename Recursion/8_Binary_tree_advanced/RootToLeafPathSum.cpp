// Root to Leaf Path Sum
// Problem - Find all root-to-leaf paths with a sum equal to target.

// What it means
// Collect all paths from root to leaves where the sum of node values equals target.

// Constraints
// - Tree nodes: 0 <= n <= 5000
// - Node values: -1000 <= val <= 1000
// - Target: -1000 <= target <= 1000
// - Time complexity: O(n)
// - Space complexity: O(h)

// Example
// Input: [5,4,8,11,null,13,4,7,2,null,1], targetSum = 22
// Output: [[5,4,11,2],[5,8,4,5]]

// Test cases
// [5,4,8,11,null,13,4,7,2,null,1], target = 22 -> 2 paths
// [1,2,3], target = 6 -> [[1,2,3]]

