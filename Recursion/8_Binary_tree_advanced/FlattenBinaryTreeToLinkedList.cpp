// Flatten Binary Tree to Linked List
// Problem - Flatten a binary tree into a linked list (in-place).

// What it means
// Convert the tree into a right-skewed structure using preorder traversal.

// Constraints
// - Tree nodes: 0 <= n <= 2000
// - Node values: -100 <= val <= 100
// - Time complexity: O(n)
// - Space complexity: O(h) recursion depth

// Example
// Input: [1,2,5,3,4,null,6]
// Output: [1,null,2,null,3,null,4,null,5,null,6]

// Test cases
// [] -> []
// [1] -> [1]
// [1,2,5,3,4,null,6] -> flattened list

