// All Root to Leaf Paths
// Problem - Find all paths from root to leaf nodes in a binary tree.

// What it means
// Collect all possible paths that go from the root node to each leaf node.

// Constraints
// - Tree nodes: 1 <= n <= 1000
// - Node values: -9 <= val <= 9
// - Time complexity: O(n)
// - Space complexity: O(h) where h is height

// Example
// Input: [1,2,3]
// Output: ["1->2", "1->3"]

// Test cases
// [1] -> ["1"]
// [1,2,3] -> ["1->2", "1->3"]
// [1,2,null,3] -> ["1->2->3"]

