// Lowest Common Ancestor
// Problem - Find the lowest common ancestor (LCA) of two nodes in a BST.

// What it means
// Find the deepest node that is an ancestor of both given nodes.

// Constraints
// - Tree nodes: 2 <= n <= 10^5
// - Node values: -10^9 <= val <= 10^9
// - Time complexity: O(h) where h is height
// - Space complexity: O(h)

// Example
// Input: root = [6,2,8,0,4,7,9,null,null,3,5], p = 2, q = 8
// Output: 6

// Test cases
// [6,2,8,0,4,7,9,null,null,3,5], p = 2, q = 8 -> 6
// [6,2,8,0,4,7,9,null,null,3,5], p = 2, q = 4 -> 2

