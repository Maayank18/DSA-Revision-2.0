// Combination Sum
// Problem - Find all unique combinations in a given array that sum to a target number.

// What it means
// Generate all combinations where elements can be reused and the sum equals the target.

// Constraints
// - Array length: 1 <= n <= 30
// - Target: 1 <= target <= 500
// - Array elements: positive integers
// - Time complexity: O(N^(T/M)) where T is target and M is min element

// Example
// Input: arr = [2, 3, 6, 7], target = 7
// Output: [[2,2,3], [7]]

// Test cases
// [], target = 0 -> []
// [2], target = 2 -> [[2]]
// [2, 3, 6, 7], target = 7 -> [[2,2,3], [7]]
// [5, 10], target = 5 -> [[5]]

