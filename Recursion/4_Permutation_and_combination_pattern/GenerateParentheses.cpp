// Generate Parentheses
// Problem - Generate all combinations of well-formed parentheses with n pairs.

// What it means
// Create all valid combinations of opening and closing parentheses.

// Constraints
// - 1 <= n <= 8
// - Total valid combinations: nth Catalan number
// - Time complexity: O(4^n / sqrt(n))

// Example
// Input: n = 3
// Output: ["((()))", "(()())", "(())()", "()(())", "()()()"]

// Test cases
// n = 1 -> ["()"]
// n = 2 -> ["(())", "()()"]
// n = 3 -> ["((()))", "(()())", "(())()", "()(())", "()()()"]

