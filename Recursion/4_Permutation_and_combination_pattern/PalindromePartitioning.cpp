// Palindrome Partitioning
// Problem - Given a string, partition it such that every substring of the partition is a palindrome.

// What it means
// Split a string into parts where each part reads the same forward and backward.

// Constraints
// - String length: 1 <= n <= 16
// - Time complexity: O(N * 2^N) where N is length of string

// Example
// Input: "nitin"
// Output: [["n","i","t","i","n"], ["nitin"]]

// Test cases
// "a" -> [["a"]]
// "ab" -> [["a","b"]]
// "nitin" -> [["n","i","t","i","n"], ["nitin"]]
// "aab" -> [["a","a","b"], ["aa","b"]]

