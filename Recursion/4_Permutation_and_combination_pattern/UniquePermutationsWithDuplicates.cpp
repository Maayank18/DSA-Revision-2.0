// Unique Permutations with Duplicates
// Problem - Generate all unique permutations of a given array that may contain duplicates.

// What it means
// Create all unique orderings of array elements while handling duplicate values.

// Constraints
// - Array length: 1 <= n <= 8
// - Array may contain duplicate elements
// - Time complexity: O(n * n!)

// Example
// Input: [1, 1, 2]
// Output: [[1,1,2], [1,2,1], [2,1,1]]

// Test cases
// [1] -> [[1]]
// [1, 1] -> [[1,1]]
// [1, 1, 2] -> [[1,1,2], [1,2,1], [2,1,1]]
// [1, 2, 2] -> [[1,2,2], [2,1,2], [2,2,1]]

